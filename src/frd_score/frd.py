"""Calculates the Frechet Radiomics Distance (FRD) to compare image biomarker distributions.

The FRD metric calculates the distance between two distributions of biomarkers extracted from imaging data.
Typically, one of the two distributions is generated by a generative model such as GANs or Diffusion models.
When run as a stand-alone program, it compares the distribution of images that are stored as PNG/JPEG/NIfTI at
a specified location.

This implementation supports two versions of FRD:
  - FRDv0 (Osuala et al., 2024): ~94 radiomic features, min-max normalization, raw Fréchet distance.
  - FRDv1 (Konz, Osuala et al., 2026): ~464 radiomic features (including wavelet/LoG filters), z-score
    normalization w.r.t. D1, log-transformed Fréchet distance. Default version.

Reference: Konz, Osuala, et al. "Fréchet Radiomic Distance (FRD): A Versatile Metric for Comparing
Medical Imaging Datasets." Medical Image Analysis, 103943 (2026). https://arxiv.org/abs/2412.01496

Some code from https://github.com/mseitzer/pytorch-fid and https://github.com/bioinf-jku/TTUR was reused and adapted.

Usage:
    python -m frd_score dir1 dir2 [--frd_version v1]
"""

