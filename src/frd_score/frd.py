def detect_ood(
    feature_list,
    detection_type="image",
    val_frac=0.1,
    use_val_set=False,
    id_dist_assumption="gaussian",
    output_dir="outputs/ood_predictions",
    seed=None,
    filenames=None,
):
    """Out-of-distribution detection using radiomics features.

    Params:
    -- feature_list       : list of two np.ndarray [D1_features, D2_features],
                            where D1 is in-distribution (reference) and D2 is test.
    -- detection_type     : 'image' for per-image scoring, 'dataset' for nFRD.
    -- val_frac           : fraction of D1 to hold out for threshold estimation.
    -- use_val_set        : if True, hold out val_frac of D1 for threshold estimation.
                            If False (default), use the full reference set for both
                            the mean embedding and threshold estimation (no split).
    -- id_dist_assumption : 'gaussian', 't', or 'counting' for threshold estimation.
    -- output_dir         : directory for saving OOD prediction CSVs.
    -- seed               : random seed for reproducibility (val split, sample matching).
    -- filenames          : list of filenames for the test set images (for CSV output).

    Returns:
    -- dict with results (scores, predictions, nfrd, etc.)
    """
    from scipy import stats as sps

    rng = np.random.default_rng(seed)

    feats_ref = feature_list[0]
    feats_test = feature_list[1]
    results = {}

    if use_val_set:
        # Split reference into train/val
        n_ref = feats_ref.shape[0]
        n_val = max(1, int(val_frac * n_ref))
        val_idx = rng.choice(n_ref, n_val, replace=False)
        train_idx = np.array([i for i in range(n_ref) if i not in val_idx])
        ref_train = feats_ref[train_idx]
        ref_val = feats_ref[val_idx]
    else:
        # No hold-out: use full reference for both mean and threshold estimation
        ref_train = feats_ref
        ref_val = feats_ref

    # Compute mean of reference training set
    ref_mean = ref_train.mean(axis=0)

    # L2 scores
    test_scores = np.array([np.linalg.norm(ref_mean - x) for x in feats_test])
    val_scores = np.array([np.linalg.norm(ref_mean - x) for x in ref_val])

    if detection_type == "image":
        mu_val, sigma_val = np.mean(val_scores), np.std(val_scores)
        dof = len(val_scores) - 1

        if id_dist_assumption == "gaussian":
            threshold = sigma_val * sps.norm.ppf(0.95) + mu_val
            z = (test_scores - mu_val) / max(sigma_val, 1e-10)
            p_values = 1 - sps.norm.cdf(z)
        elif id_dist_assumption == "t":
            threshold = sigma_val * sps.t.ppf(0.95, max(dof, 1)) + mu_val
            t_stat = (test_scores - mu_val) / max(
                sigma_val / np.sqrt(len(val_scores)), 1e-10
            )
            p_values = 1 - sps.t.cdf(t_stat, max(dof, 1))
        elif id_dist_assumption == "counting":
            threshold = np.percentile(val_scores, 95)
            p_values = np.array(
                [np.sum(val_scores > score) / len(val_scores) for score in test_scores]
            )
        else:
            raise ValueError(f"Unknown id_dist_assumption: {id_dist_assumption}")

        predictions = test_scores > threshold
        results["threshold"] = float(threshold)
        results["scores"] = test_scores
        results["predictions"] = predictions
        results["p_values"] = p_values

        # Save to CSV
        os.makedirs(output_dir, exist_ok=True)
        csv_path = os.path.join(output_dir, "ood_predictions.csv")
        with open(csv_path, "w", newline="") as f:
            writer = csv.writer(f)
            if filenames is not None:
                writer.writerow(["filename", "ood_score", "ood_prediction", "p_value"])
                for i in range(len(test_scores)):
                    fname = filenames[i] if i < len(filenames) else str(i)
                    writer.writerow(
                        [fname, test_scores[i], bool(predictions[i]), p_values[i]]
                    )
            else:
                writer.writerow(["index", "ood_score", "ood_prediction", "p_value"])
                for i in range(len(test_scores)):
                    writer.writerow(
                        [i, test_scores[i], bool(predictions[i]), p_values[i]]
                    )
        print(f"OOD predictions saved to {csv_path}")
        print(
            f"Threshold: {threshold:.4f}, OOD detected: {int(predictions.sum())}/{len(predictions)}"
        )

    elif detection_type == "dataset":
        try:
            from sklearn.metrics import roc_auc_score
        except ImportError:
            raise ImportError(
                "Dataset-level OOD detection requires scikit-learn. "
                "Install with: pip install scikit-learn"
            )

        all_scores = np.concatenate([val_scores, test_scores])
        all_labels = np.concatenate(
            [np.zeros(len(val_scores)), np.ones(len(test_scores))]
        )
        auc_dev = 2.0 * abs(roc_auc_score(all_labels, all_scores) - 0.5)
        results["nfrd"] = float(auc_dev)
        print(f"Dataset-level OOD score (nFRD): {auc_dev:.4f}")

    else:
        raise ValueError(
            f"detection_type must be 'image' or 'dataset', got '{detection_type}'."
        )

    return results


