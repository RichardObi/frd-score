def resize_image_array(sitk_image_array, resize_size, interpolation=cv2.INTER_LINEAR):
    """Resize a 2D or 3D numpy array in the spatial dims.

    resize_size can be an int (square) or a (width, height) tuple.
    """
    if isinstance(resize_size, (list, tuple)):
        target_w, target_h = resize_size
    else:
        target_w, target_h = resize_size, resize_size

    if len(sitk_image_array.shape) == 2:
        sitk_image_array_resized = cv2.resize(
            sitk_image_array, (target_w, target_h), interpolation=interpolation
        )
    elif len(sitk_image_array.shape) == 3:
        # Going through z axis (index 0 in sitk array) and resizing each slice
        sitk_image_array_resized = np.zeros(
            (sitk_image_array.shape[0], target_h, target_w)
        )
        for j in range(sitk_image_array.shape[0]):
            sitk_image_array_resized[j] = cv2.resize(
                sitk_image_array[j],
                (target_w, target_h),
                interpolation=interpolation,
            )
    else:
        raise ValueError(
            f"SITK Image array has an unexpected shape: {sitk_image_array.shape}. "
            f"Expected 2D or 3D array (no channel dim). Got {len(sitk_image_array.shape)}D"
        )
    return sitk_image_array_resized


def _detect_image_dimensionality(files):
    """Detect whether the input images are 2D or 3D by reading the first file.

    Returns:
        int: 2 for 2D images, 3 for 3D images.
    """
    first_file = files[0] if not isinstance(files[0], list) else files[0][0]
    sitk_image = sitk.ReadImage(str(first_file), outputPixelType=sitk.sitkFloat32)
    ndim = sitk_image.GetDimension()
    arr = sitk.GetArrayViewFromImage(sitk_image)
    # SimpleITK adds a dimension for e.g. single-slice images
    # Use array shape to determine actual dimensionality
    if arr.ndim == 2 or (arr.ndim == 3 and arr.shape[0] == 1):
        return 2
    elif arr.ndim == 3 and arr.shape[0] > 1:
        return 3
    else:
        return ndim


