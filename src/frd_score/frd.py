#  Main API: compute_frd
# ─────────────────────────────────────────────────────────────────────────────


def compute_frd(
    paths,
    frd_version=FRD_VERSION_DEFAULT,
    features=None,
    norm_type=None,
    norm_range=None,
    paths_masks=None,
    resize_size=None,
    verbose=False,
    save_features=False,
    norm_ref=NORM_REF_DEFAULT,
    num_workers=None,
    image_types=None,
    use_paper_log=False,
    log_sigma=None,
    config_path=None,
    bin_width=None,
    normalize_scale=None,
    voxel_array_shift=None,
    exclude_features=None,
    match_sample_count=False,
    means_only=False,
    settings_dict=None,
    interpret=False,
    interpret_dir="outputs/interpretability_visualizations",
):
    """Compute the Fréchet Radiomics Distance between two image distributions.

    This is the main entry point. Supports both FRDv0 and FRDv1 (default).
    All parameters work for both versions unless noted otherwise.

    Params:
    -- paths                : List of two paths (directories or file lists) or .npz files.
    -- frd_version          : 'v0' or 'v1' (default). Determines default feature extraction,
                              normalization, and post-processing strategy.
    -- features             : List of feature class names (e.g. ['firstorder', 'glcm']).
                              For v0: defaults to all 8 classes. For v1: None uses the
                              YAML-configured classes; if specified, overrides the YAML.
    -- norm_type            : 'minmax' or 'zscore'. Default depends on frd_version.
    -- norm_range           : [min, max] for normalization rescaling. Default depends on frd_version.
    -- paths_masks          : Optional list of two mask directory paths.
    -- resize_size          : Optional pixel resize target (int or (w,h) tuple).
    -- verbose              : Logging verbosity.
    -- save_features        : Save features to CSV (default False).
    -- norm_ref             : str, one of 'joint' (default), 'd1', or 'independent'.
    -- num_workers          : CPU workers for multiprocessing (None = auto).
    -- image_types          : List of pyradiomics image types, e.g. ['Original', 'LoG', 'Wavelet'].
    -- use_paper_log        : bool. If True, apply paper Eq. 3 log transform: FRD = log(d_F).
                              Default False uses the original code convention: FRD = log(d_F^2).
    -- log_sigma            : list of float, LoG sigma values (e.g. [2.0, 3.0, 4.0, 5.0]).
    -- config_path          : str, path to a custom pyradiomics YAML config file.
                              When provided, overrides feature_groups, image_types, log_sigma,
                              bin_width, and other extractor settings entirely.
    -- bin_width            : int, pyradiomics binWidth setting.
    -- normalize_scale      : float, pyradiomics normalizeScale setting.
    -- voxel_array_shift    : float, pyradiomics voxelArrayShift setting.
    -- exclude_features     : list of str, feature categories to exclude after extraction.
                              Options: 'textural', 'wavelet', 'firstorder'.
    -- match_sample_count   : bool, if True subsample the larger dataset to match the smaller.
    -- means_only           : bool, if True compute FRD using only mean vectors (no covariance).
    -- settings_dict        : dict, arbitrary pyradiomics settings passed directly to extractor.
    -- interpret            : bool, if True run interpretability analysis after FRD computation.
    -- interpret_dir        : str, directory for interpretability output files.

    Returns:
    -- frd_value : The computed FRD score. For v1 this is log-transformed.
    """
    if frd_version not in FRD_VERSIONS:
        raise ValueError(
            f"Unknown frd_version '{frd_version}'. Must be one of {sorted(FRD_VERSIONS)}."
        )

    norm_type, norm_range, features, image_types, norm_ref = _resolve_defaults(
        frd_version, norm_type, norm_range, features, image_types, norm_ref=norm_ref
    )

    if norm_ref not in NORM_REF_OPTIONS:
        raise ValueError(
            f"Unknown norm_ref '{norm_ref}'. Must be one of {sorted(NORM_REF_OPTIONS)}."
        )

    if norm_ref == NORM_REF_INDEPENDENT:
        warnings.warn(
            "norm_ref='independent': Each dataset's features are normalized using only "
            "their own statistics. This differs from the default joint normalization and "
            "the paper's D1-reference normalization (Eq. 3), and may reduce comparability "
            "of FRD values across experiments.",
            UserWarning,
            stacklevel=2,
        )

    _ensure_radiomics()

    if not verbose:
        logger = logging.getLogger("radiomics")
        logger.setLevel(logging.ERROR)
        setVerbosity(60)

    for p in paths:
        if not isinstance(p, list) and not os.path.exists(p):
            raise RuntimeError(
                f"Invalid path: {p}. Provide a valid directory or list of files."
            )

    if (
        norm_ref != NORM_REF_INDEPENDENT
        and not isinstance(paths[0], list)
        and paths[0].endswith(".npz")
    ):
        raise ValueError(
            f"norm_ref='{norm_ref}' is not supported with .npz files (statistics already computed). "
            "Use norm_ref='independent' or provide image paths."
        )

    # Detect 2D vs 3D for v1 config selection
    image_dim = 2  # default
    if frd_version == FRD_VERSION_V1:
        # Detect dimensionality from the first path/file
        _first_path = paths[0]
        if isinstance(_first_path, list):
            _files_for_detection = _first_path
        else:
            _first_path_obj = pathlib.Path(_first_path)
            _files_for_detection = sorted(
                [
                    f
                    for ext in IMAGE_EXTENSIONS
                    for f in _first_path_obj.glob(f"*.{ext}")
                ]
            )
        if _files_for_detection:
            image_dim = _detect_image_dimensionality(_files_for_detection)
            if verbose:
                logging.info(
                    f"Detected {image_dim}D images. Using corresponding v1 config."
                )

    feature_extractor = get_feature_extractor(
        frd_version=frd_version,
        features=features,
        image_dim=image_dim,
        image_types=image_types,
        log_sigma=log_sigma,
        config_path=config_path,
        bin_width=bin_width,
        normalize_scale=normalize_scale,
        voxel_array_shift=voxel_array_shift,
    )

    # Apply arbitrary user settings on top
    if settings_dict:
        for key, val in settings_dict.items():
            feature_extractor.settings[key] = val

    mu_list, sigma_list, normalized_feature_list, feature_names = (
        compute_statistics_of_paths(
            paths,
            norm_type,
            norm_range,
            feature_extractor,
            paths_mask=paths_masks,
            resize_size=resize_size,
            verbose=verbose,
            save_features=save_features,
            norm_ref=norm_ref,
            num_workers=num_workers,
            exclude_features=exclude_features,
            match_sample_count=match_sample_count,
            frd_version=frd_version,
        )
    )

    if verbose:
        logging.debug(f"mu_list shapes: {[m.shape for m in mu_list]}")

    frd_value = calculate_frechet_distance(
        mu_list[0],
        sigma_list[0],
        mu_list[1],
        sigma_list[1],
        means_only=means_only,
    )

    # v1 applies log transform for stability.
    if frd_version == FRD_VERSION_V1:
        if use_paper_log:
            warnings.warn(
                "use_paper_log=True: Computing FRD as log(sqrt(d_F^2)) per paper Eq. 3. "
                "This produces values that are HALF of the default (log(d_F^2)) and are "
                "NOT directly comparable to previously published FRD values or experiments "
                "that used the default setting. Only use this if you specifically need "
                "paper-formula consistency.",
                UserWarning,
                stacklevel=2,
            )
            # Paper formula: FRD = log(d_F) = log(sqrt(d_F^2)) = 0.5 * log(d_F^2)
            frd_value = np.log(np.sqrt(frd_value)) if frd_value > 0 else float("-inf")
        else:
            # Original code convention: FRD = log(d_F^2)
            frd_value = np.log(frd_value) if frd_value > 0 else float("-inf")

    print(f"FRD ({frd_version}): {frd_value}")

    # ── Interpretability analysis ──
    if interpret:
        interpret_frd(
            normalized_feature_list,
            feature_names,
            viz_dir=interpret_dir,
        )

    return frd_value


