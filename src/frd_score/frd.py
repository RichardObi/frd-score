#  Save stats (.npz)
# ─────────────────────────────────────────────────────────────────────────────


def save_frd_stats(
    paths,
    frd_version=FRD_VERSION_DEFAULT,
    features=None,
    norm_type=None,
    norm_range=None,
    paths_masks=None,
    resize_size=None,
    verbose=False,
    save_features=False,
    norm_ref=NORM_REF_DEFAULT,
    num_workers=None,
    image_types=None,
    use_paper_log=False,
    log_sigma=None,
    config_path=None,
    bin_width=None,
    normalize_scale=None,
    voxel_array_shift=None,
    exclude_features=None,
    match_sample_count=False,
    settings_dict=None,
):
    """Compute and save feature statistics for a single distribution to a .npz file.

    Note: use_paper_log is accepted for API consistency with compute_frd() but
    has no effect here since save_frd_stats only stores mu/sigma, not the final
    FRD score.
    """

    norm_type, norm_range, features, image_types, norm_ref = _resolve_defaults(
        frd_version, norm_type, norm_range, features, image_types, norm_ref=norm_ref
    )

    if norm_ref not in NORM_REF_OPTIONS:
        raise ValueError(
            f"Unknown norm_ref '{norm_ref}'. Must be one of {sorted(NORM_REF_OPTIONS)}."
        )

    if not isinstance(paths[0], list) and not os.path.exists(paths[0]):
        raise RuntimeError(f"Invalid input path: {paths[0]}")
    if os.path.exists(paths[1]):
        raise RuntimeError(f"Output .npz file already exists: {paths[1]}")
    elif not paths[1].endswith(".npz"):
        logging.warning(
            f"Output path '{paths[1]}' does not have .npz extension. Continuing anyway."
        )

    # Detect 2D vs 3D
    image_dim = 2
    if frd_version == FRD_VERSION_V1:
        _first_path = paths[0]
        if isinstance(_first_path, list):
            _files_for_detection = _first_path
        else:
            _first_path_obj = pathlib.Path(_first_path)
            _files_for_detection = sorted(
                [
                    f
                    for ext in IMAGE_EXTENSIONS
                    for f in _first_path_obj.glob(f"*.{ext}")
                ]
            )
        if _files_for_detection:
            image_dim = _detect_image_dimensionality(_files_for_detection)

    feature_extractor = get_feature_extractor(
        frd_version=frd_version,
        features=features,
        image_dim=image_dim,
        image_types=image_types,
        log_sigma=log_sigma,
        config_path=config_path,
        bin_width=bin_width,
        normalize_scale=normalize_scale,
        voxel_array_shift=voxel_array_shift,
    )

    # Apply arbitrary user settings on top
    if settings_dict:
        for key, val in settings_dict.items():
            feature_extractor.settings[key] = val

    if verbose:
        logging.info(f"Computing statistics for {paths[0]}")

    mu_list, sigma_list, _, _ = compute_statistics_of_paths(
        [paths[0]],
        norm_type=norm_type,
        norm_range=norm_range,
        feature_extractor=feature_extractor,
        paths_mask=None if paths_masks is None else [paths_masks[0]],
        resize_size=resize_size,
        verbose=verbose,
        save_features=save_features,
        norm_ref=norm_ref,
        num_workers=num_workers,
        exclude_features=exclude_features,
        match_sample_count=match_sample_count,
        frd_version=frd_version,
    )

    np.savez_compressed(paths[1], mu=mu_list[0], sigma=sigma_list[0])


