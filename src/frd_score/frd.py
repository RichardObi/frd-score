def calculate_feature_statistics(
    file_lists: list,
    norm_type: str,
    norm_range: list,
    feature_extractor,
    mask_lists: list = None,
    resize_size=None,
    verbose: bool = False,
    save_features: bool = False,
    norm_ref: str = NORM_REF_DEFAULT,
    num_workers=None,
    exclude_features=None,
    match_sample_count=False,
    frd_version=FRD_VERSION_DEFAULT,
):
    """Extract features and compute per-distribution statistics (mean, covariance).

    Params:
    -- exclude_features  : list of str from EXCLUDE_OPTIONS, feature categories to drop
    -- match_sample_count: bool, if True subsample larger set to match smaller

    Returns:
    -- mu_list                : List of mean vectors for each distribution.
    -- sigma_list             : List of covariance matrices for each distribution.
    -- normalized_feature_list: List of normalized feature arrays.
    -- feature_names          : List of feature name strings.
    """
    if mask_lists is None:
        mask_lists = [None] * len(file_lists)

    feature_list = []
    feature_names = []
    all_radiomics_results = []
    all_mask_paths = []

    for idx, file_list in enumerate(file_lists):
        features, radiomics_results, mask_paths = compute_features(
            files=file_list,
            feature_extractor=feature_extractor,
            masks=mask_lists[idx] if mask_lists is not None else None,
            resize_size=resize_size,
            verbose=verbose,
            num_workers=num_workers,
            frd_version=frd_version,
        )
        if verbose:
            logging.info(f"Features shape: {features.shape}")

        # Log NaN diagnostics
        if np.isnan(features).any():
            nan_indices = np.where(np.isnan(features))
            unique_nan_indices = np.unique(nan_indices[1])
            logging.warning("NaN values detected in features array.")
            if verbose:
                for feature_idx in unique_nan_indices:
                    nan_count = np.sum(np.isnan(features[:, feature_idx]))
                    logging.info(f"Feature {feature_idx}: {nan_count} NaN values")

        feature_list.append(features)
        feature_names = list(radiomics_results[0].keys())
        all_radiomics_results.append(radiomics_results)
        all_mask_paths.append(mask_paths)

    # ── Exclude feature categories ──
    if exclude_features:
        feature_list, feature_names = _apply_exclude_features(
            feature_list, feature_names, exclude_features
        )

    # ── v1-specific preprocessing (paper-exact pipeline) ──
    # The original v1 converted features to float32 (via pandas .to_numpy().astype(np.float32))
    # and dropped rows with any NaN (via pandas .dropna()) *before* normalization.
    # We replicate this exactly for v1 mode so the final FRD is identical to the paper's results.
    if frd_version == FRD_VERSION_V1:
        for idx in range(len(feature_list)):
            feats = feature_list[idx]
            # Drop rows (images) with any NaN feature value
            nan_row_mask = np.isnan(feats).any(axis=1)
            if nan_row_mask.any():
                n_dropped = int(nan_row_mask.sum())
                logging.info(
                    f"v1 compat: dropping {n_dropped} image(s) with NaN features "
                    f"from distribution {idx} ({feats.shape[0] - n_dropped} remaining)."
                )
                feats = feats[~nan_row_mask]
            # Cast to float32 (matching original v1's pandas→numpy→float32 conversion)
            feature_list[idx] = feats.astype(np.float32)

    # ── Match sample counts ──
    if match_sample_count:
        feature_list = _apply_match_sample_count(feature_list)

    mu_list = []
    sigma_list = []
    normalized_feature_list = []

    base_distribution = None
    if norm_ref == NORM_REF_JOINT:
        base_distribution = np.concatenate(feature_list, axis=0)
    elif norm_ref == NORM_REF_D1:
        base_distribution = feature_list[0]
    # NORM_REF_INDEPENDENT: base_distribution stays None → each set uses own stats

    # For v1 mode, use "raw" z-score: don't special-case std=0 and don't replace
    # NaN post-normalization, so that inf/nan values propagate to the column filter.
    # This matches the original v1 pipeline: ``(x - mean) / std`` with no guards.
    v1_zscore_compat = frd_version == FRD_VERSION_V1

    for idx, features in enumerate(feature_list):
        if norm_type == "minmax":
            normalized_features = min_max_normalize(
                features=features,
                new_min=norm_range[0],
                new_max=norm_range[1],
                feature_names=feature_names,
                base_distribution=base_distribution,
            )
        elif norm_type == "zscore":
            normalized_features = z_score_normalize(
                features=features,
                new_min=norm_range[0],
                new_max=norm_range[1],
                feature_names=feature_names,
                base_distribution=base_distribution,
                replace_nan=not v1_zscore_compat,
                handle_std_zero=not v1_zscore_compat,
            )
        else:
            raise ValueError(
                f"Normalization type '{norm_type}' is not supported. Use 'minmax' or 'zscore'."
            )
        mu_list.append(np.mean(normalized_features, axis=0))
        sigma_list.append(np.cov(normalized_features, rowvar=False))
        normalized_feature_list.append(normalized_features)

    # ── Drop NaN/Inf feature columns (v1 mode) ──
    # Original v1 drops entire feature columns that have any NaN or Inf in either
    # distribution after z-score normalization. This is necessary because D1-only
    # normalization can produce Inf when std=0 for D1 but the feature varies in D2.
    if frd_version == FRD_VERSION_V1 and len(normalized_feature_list) >= 2:
        nan_inf_mask = np.zeros(normalized_feature_list[0].shape[1], dtype=bool)
        for nf in normalized_feature_list:
            nan_inf_mask |= np.isnan(nf).any(axis=0) | np.isinf(nf).any(axis=0)
        if nan_inf_mask.any():
            n_dropped = int(nan_inf_mask.sum())
            keep = ~nan_inf_mask
            logging.info(
                f"Dropping {n_dropped} feature columns with NaN/Inf after normalization "
                f"({int(keep.sum())} remaining)."
            )
            normalized_feature_list = [nf[:, keep] for nf in normalized_feature_list]
            feature_names = [fn for fn, k in zip(feature_names, keep) if k]
            # Recompute mu/sigma after dropping
            mu_list = [np.mean(nf, axis=0) for nf in normalized_feature_list]
            sigma_list = [np.cov(nf, rowvar=False) for nf in normalized_feature_list]

    if save_features:
        for idx, normalized_features in enumerate(normalized_feature_list):
            folder_name = Path(file_lists[idx][0]).parent.stem
            unique_identifier = int(time.time())
            storage_dir = str(Path(file_lists[idx][0]).parents[1])

            csv_file_path = os.path.join(
                storage_dir,
                f"radiomics_set{idx}_results_{folder_name}_{unique_identifier}.csv",
            )
            norm_csv_file_path = os.path.join(
                storage_dir,
                f"radiomics_set{idx}_results_normalized_{folder_name}_{unique_identifier}.csv",
            )
            save_features_to_csv(
                csv_file_path,
                file_lists[idx],
                all_mask_paths[idx],
                all_radiomics_results[idx],
            )
            # Save normalized features as dicts for the normalized CSV
            norm_dicts = []
            for row_idx in range(normalized_features.shape[0]):
                norm_dicts.append(
                    {
                        fn: float(normalized_features[row_idx, col_idx])
                        for col_idx, fn in enumerate(feature_names)
                    }
                )
            norm_mask_paths = (
                all_mask_paths[idx]
                if all_mask_paths[idx]
                else [None] * len(file_lists[idx])
            )
            save_features_to_csv(
                norm_csv_file_path, file_lists[idx], norm_mask_paths, norm_dicts
            )

    return mu_list, sigma_list, normalized_feature_list, feature_names


def compute_statistics_of_paths(
    paths: list,
    norm_type: str,
    norm_range: list,
    feature_extractor,
    paths_mask: list = None,
    resize_size=None,
    verbose=False,
    save_features=False,
    norm_ref=NORM_REF_DEFAULT,
    num_workers=None,
    exclude_features=None,
    match_sample_count=False,
    frd_version=FRD_VERSION_DEFAULT,
):
    """Resolve paths (directories or .npz files) and compute feature statistics."""

    if not isinstance(paths[0], list) and paths[0].endswith(".npz"):
        with np.load(paths[0]) as f:
            mu1, sigma1 = f["mu"][:], f["sigma"][:]
        if len(paths) < 2:
            return [mu1], [sigma1], [], []
        if not isinstance(paths[1], list) and paths[1].endswith(".npz"):
            with np.load(paths[1]) as f:
                mu2, sigma2 = f["mu"][:], f["sigma"][:]
            return [mu1, mu2], [sigma1, sigma2], [], []
        else:
            raise ValueError(
                "When the first path is a .npz file, the second path must also "
                "be a .npz file, or use image directories for both paths."
            )
    else:
        file_lists = []
        mask_lists = []
        for idx, path in enumerate(paths):
            if isinstance(path, list):
                file_lists.append(path)
            else:
                path = pathlib.Path(path)
                file_lists.append(
                    sorted(
                        [
                            file
                            for ext in IMAGE_EXTENSIONS
                            for file in path.glob("*.{}".format(ext))
                        ]
                    )
                )
        if paths_mask is not None:
            for idx2, path_mask in enumerate(paths_mask):
                if path_mask is None:
                    mask_lists.append(None)
                elif isinstance(path_mask, list):
                    mask_lists.append(path_mask)
                else:
                    path_mask = pathlib.Path(path_mask)
                    mask_lists.append(
                        sorted(
                            [
                                mask
                                for ext in IMAGE_EXTENSIONS
                                for mask in path_mask.glob("*.{}".format(ext))
                            ]
                        )
                    )
            for midx, (ml, fl) in enumerate(zip(mask_lists, file_lists)):
                if ml is not None and len(ml) != len(fl):
                    logging.warning(
                        f"Distribution {midx}: Number of mask files ({len(ml)}) != "
                        f"number of images ({len(fl)}). Please verify."
                    )
        else:
            mask_lists = [None] * len(paths)
            if verbose:
                logging.debug(f"file_lists: {file_lists}")

        return calculate_feature_statistics(
            file_lists=file_lists,
            norm_type=norm_type,
            norm_range=norm_range,
            feature_extractor=feature_extractor,
            mask_lists=mask_lists,
            resize_size=resize_size,
            verbose=verbose,
            save_features=save_features,
            norm_ref=norm_ref,
            num_workers=num_workers,
            exclude_features=exclude_features,
            match_sample_count=match_sample_count,
            frd_version=frd_version,
        )


# ─────────────────────────────────────────────────────────────────────────────
#  Version-aware defaults
# ─────────────────────────────────────────────────────────────────────────────


def _resolve_defaults(
    frd_version, norm_type, norm_range, features, image_types, norm_ref=None
):
    """Resolve None args to version-specific defaults."""
    if frd_version == FRD_VERSION_V0:
        if norm_type is None:
            norm_type = V0_DEFAULT_NORM_TYPE
        if norm_range is None:
            norm_range = list(V0_DEFAULT_NORM_RANGE)
        if features is None:
            features = list(V0_DEFAULT_FEATURES)
        if norm_ref is None:
            norm_ref = V0_DEFAULT_NORM_REF
        # image_types=None means "use v0 default (Original only)" — handled in extractor
    else:  # v1
        if norm_type is None:
            norm_type = V1_DEFAULT_NORM_TYPE
        if norm_range is None:
            norm_range = list(V1_DEFAULT_NORM_RANGE)
        if norm_ref is None:
            norm_ref = V1_DEFAULT_NORM_REF
        # features=None means "use YAML defaults" for v1; user-specified overrides YAML
        # image_types=None means "use YAML defaults (Original+LoG+Wavelet)" — handled in extractor
    return norm_type, norm_range, features, image_types, norm_ref


