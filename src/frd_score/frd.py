def get_feature_extractor(
    frd_version: str = FRD_VERSION_DEFAULT,
    features=None,
    settings_dict: dict = None,
    image_dim: int = 2,
    image_types=None,
    log_sigma=None,
    config_path=None,
    bin_width=None,
    normalize_scale=None,
    voxel_array_shift=None,
):
    """Factory function that returns the appropriate pyradiomics feature extractor.

    After creating the base extractor (dict-based for v0, YAML-based for v1),
    unified overrides for feature classes and image types are applied.

    Params:
    -- frd_version      : 'v0' or 'v1'
    -- features         : list of feature class names to extract. For v0, defaults to
                          V0_DEFAULT_FEATURES. For v1, None means use YAML defaults;
                          if specified, overrides the YAML feature classes.
    -- settings_dict    : (v0 only) optional radiomics settings dict override
    -- image_dim        : 2 or 3, used to pick the correct YAML config in v1 mode
    -- image_types      : list of image type names (e.g. ['Original', 'LoG', 'Wavelet']).
                          None means use version defaults. If specified, overrides
                          the image types for either version.
    -- log_sigma        : list of float sigma values for LoG filter.
                          None means use V1_DEFAULT_LOG_SIGMA.
    -- config_path      : path to a custom PyRadiomics YAML config (v1 only).
                          When provided, overrides the built-in YAML configs.
    -- bin_width        : int, PyRadiomics binWidth setting override.
    -- normalize_scale  : float, PyRadiomics normalizeScale setting override.
    -- voxel_array_shift: float, PyRadiomics voxelArrayShift setting override.

    Returns:
    -- A RadiomicsFeatureExtractor instance.
    """
    if log_sigma is None:
        log_sigma = list(V1_DEFAULT_LOG_SIGMA)

    if frd_version == FRD_VERSION_V0:
        if features is None:
            features = V0_DEFAULT_FEATURES
        extractor = get_feature_extractor_v0(features, settings_dict)
    elif frd_version == FRD_VERSION_V1:
        if config_path is not None:
            extractor = get_feature_extractor_v1(config_path)
        else:
            _config_path = V1_CONFIG_2D if image_dim == 2 else V1_CONFIG_3D
            extractor = get_feature_extractor_v1(_config_path)
        # For v1, apply feature class override if user explicitly specified feature_groups
        if features is not None and config_path is None:
            desired = set(features)
            # Snapshot YAML-configured features (value is list or None=all)
            yaml_features = dict(extractor.enabledFeatures)
            extractor.disableAllFeatures()
            for cls_name in desired:
                if cls_name in yaml_features:
                    # Restore the YAML's specific feature selection for this class
                    # enableFeaturesByName takes **kwargs: className=[featureNames] or None=all
                    extractor.enableFeaturesByName(
                        **{cls_name: yaml_features[cls_name]}
                    )
                else:
                    # Class was not in the YAML config — enable all features in it
                    extractor.enableFeatureClassByName(cls_name)
    else:
        raise ValueError(
            f"Unknown frd_version '{frd_version}'. Must be one of {sorted(FRD_VERSIONS)}."
        )

    # Apply image type override (works for both v0 and v1, skipped if config_path given)
    if image_types is not None and config_path is None:
        extractor.disableAllImageTypes()
        for img_type in image_types:
            if img_type == "LoG":
                extractor.enableImageTypeByName("LoG", customArgs={"sigma": log_sigma})
            else:
                extractor.enableImageTypeByName(img_type)
    elif config_path is None:
        # Even without an image_types override, apply log_sigma if LoG is enabled
        # (user may have supplied --log_sigma without --image_types)
        enabled_types = extractor.enabledImagetypes
        if "LoG" in enabled_types:
            extractor.enableImageTypeByName("LoG", customArgs={"sigma": log_sigma})

    # Apply PyRadiomics setting overrides
    if bin_width is not None and config_path is None:
        extractor.settings["binWidth"] = bin_width
    if normalize_scale is not None and config_path is None:
        extractor.settings["normalizeScale"] = normalize_scale
    if voxel_array_shift is not None and config_path is None:
        extractor.settings["voxelArrayShift"] = voxel_array_shift

    return extractor


# ─────────────────────────────────────────────────────────────────────────────
#  Per-image feature extraction
# ─────────────────────────────────────────────────────────────────────────────


def process_image_mask_pair(
    image_mask_pair,
    resize_size=None,
    feature_extractor=None,
    verbose=False,
    frd_version=FRD_VERSION_DEFAULT,
):
    """Extract radiomics features from an (image, mask) pair.

    Returns a tuple of (feature_values_list, feature_dict, num_features) or None on failure.
    """
    image_path, mask_path = image_mask_pair

    sitk_image = sitk.ReadImage(str(image_path), outputPixelType=sitk.sitkFloat32)

    if mask_path is None:
        # Create full-image mask when no mask file is provided
        # https://discourse.slicer.org/t/features-extraction/11047/3
        ma_arr = np.ones(sitk_image.GetSize()[::-1]).astype(np.uint8)
        # Prevent pyradiomics bug: set corner voxel to 0
        # https://github.com/AIM-Harvard/pyradiomics/issues/765#issuecomment-1116713745
        # Only for v1 mode — original v0 did not apply this workaround.
        if frd_version != FRD_VERSION_V0:
            if ma_arr.ndim == 2:
                ma_arr[0][0] = 0
            elif ma_arr.ndim >= 3:
                ma_arr[0][0][0] = 0
        sitk_mask = sitk.GetImageFromArray(ma_arr)
        try:
            sitk_mask.CopyInformation(sitk_image)
        except Exception as e:
            logging.debug(
                f"Error while trying to copy information from image to mask: {e}"
            )
        if verbose:
            logging.debug(
                "Empty mask (true everywhere except corner) is used for feature extraction."
            )
    else:
        sitk_mask = sitk.ReadImage(str(mask_path))

    # Rescale [0, 255] masks to [0, 1]
    if np.max(sitk.GetArrayViewFromImage(sitk_mask)) == 255:
        sitk_mask = sitk.Cast(sitk_mask, sitk.sitkFloat32) / 255.0

    if resize_size is not None:
        sitk_image_array = sitk.GetArrayViewFromImage(sitk_image)
        sitk_image_array_resized = resize_image_array(
            sitk_image_array, resize_size, interpolation=cv2.INTER_LINEAR
        )
        sitk_image_resized = sitk.GetImageFromArray(sitk_image_array_resized)
        try:
            sitk_image_resized.CopyInformation(sitk_image)
        except Exception:
            pass
        sitk_image = sitk_image_resized

        sitk_mask_array = sitk.GetArrayViewFromImage(sitk_mask)
        sitk_mask_array_resized = resize_image_array(
            sitk_mask_array, resize_size, interpolation=cv2.INTER_LINEAR
        )
        # Threshold after resizing
        sitk_mask_array_resized[sitk_mask_array_resized > 0.5] = 1
        sitk_mask_array_resized[sitk_mask_array_resized <= 0.5] = 0
        sitk_mask_resized = sitk.GetImageFromArray(sitk_mask_array_resized)
        try:
            sitk_mask_resized.CopyInformation(sitk_mask)
        except Exception:
            pass
        sitk_mask = sitk_mask_resized

    # Skip masks with <=1 segmented voxel
    if np.sum(sitk.GetArrayViewFromImage(sitk_mask)) <= 1:
        if verbose:
            logging.warning(
                f"Skipping mask with ≤1 segmented voxel (resize_size={resize_size}): {mask_path}"
            )
        return None

    # Ensure 2D images are expanded to 3D (1, H, W) for v1 YAML configs
    # that require 3D resampledPixelSpacing with force2D=true.
    # Only for v1 mode — original v0 passes 2D images directly to pyradiomics.
    img_arr = sitk.GetArrayFromImage(sitk_image)
    if img_arr.ndim == 2 and frd_version != FRD_VERSION_V0:
        img_arr_3d = img_arr[np.newaxis, ...]
        sitk_image_3d = sitk.GetImageFromArray(img_arr_3d)
        # Preserve original spacing, add z-spacing of 1.0
        orig_spacing = sitk_image.GetSpacing()
        sitk_image_3d.SetSpacing((*orig_spacing, 1.0))
        sitk_image = sitk_image_3d

        mask_arr = sitk.GetArrayFromImage(sitk_mask)
        if mask_arr.ndim == 2:
            mask_arr_3d = mask_arr[np.newaxis, ...]
            sitk_mask_3d = sitk.GetImageFromArray(mask_arr_3d)
            sitk_mask_3d.SetSpacing((*orig_spacing, 1.0))
            sitk_mask = sitk_mask_3d

    # Run feature extraction
    try:
        output = feature_extractor.execute(sitk_image, sitk_mask)
    except Exception as e:
        logging.error(
            f"Error extracting features for image {image_path} / mask {mask_path}: {e}"
        )
        raise e

    radiomics_features = {}
    for feature_name in output.keys():
        if "diagnostics" not in feature_name:
            radiomics_features[feature_name.replace("original_", "")] = float(
                output[feature_name]
            )
        elif frd_version == FRD_VERSION_V1:
            # Original v1 stored all pyradiomics output in a DataFrame, then
            # dropped string-typed and shape2D columns.  Numeric diagnostics
            # (e.g. Image-original_Mean, Mask-original_VoxelNum) survived this
            # filter and were included in the feature vector used to compute FRD
            # in the paper.  We replicate this for exact paper reproducibility.
            try:
                val = float(output[feature_name])
                radiomics_features[feature_name] = val
            except (ValueError, TypeError):
                pass  # skip string diagnostics
    radiomics_feature_values_list = list(radiomics_features.values())

    return (
        radiomics_feature_values_list,
        radiomics_features,
        len(radiomics_feature_values_list),
    )


# ─────────────────────────────────────────────────────────────────────────────
#  Batch feature extraction
# ─────────────────────────────────────────────────────────────────────────────


def compute_features(
    files,
    feature_extractor,
    masks=None,
    resize_size=None,
    verbose=False,
    num_workers=None,
    frd_version=FRD_VERSION_DEFAULT,
):
    """Compute radiomics features for a list of image files.

    Params:
    -- files              : Image file paths
    -- feature_extractor  : RadiomicsFeatureExtractor instance
    -- masks              : Corresponding mask file paths (or None for full-image masks)
    -- resize_size        : Optional resize target
    -- verbose            : Logging verbosity
    -- num_workers        : CPU workers for multiprocessing (None = auto)
    -- frd_version        : 'v0' or 'v1', controls image preprocessing behavior

    Returns:
    -- pred_arr             : np.ndarray of shape (num_images, num_features)
    -- radiomics_dicts_list : list of feature name->value dicts
    -- mask_paths           : list of mask paths used
    """
    mask_paths = []
    for idx, file_path in enumerate(files):
        if masks is not None:
            try:
                mask_paths.append(masks[idx])
            except IndexError as e:
                raise RuntimeError(
                    f"Mask '{idx}' not found for image '{file_path}'. "
                    f"Ensure each image has a mask and names correspond via sorting. "
                    f"Exception: {e}"
                )
        else:
            mask_paths.append(None)

    if verbose:
        logging.info(
            f"Processing {len(files)} image-mask pairs (e.g., IMG[0]:{files[0]}, MASK[0]: {mask_paths[0]})"
        )

    # Multiprocessing
    if num_workers is None:
        num_workers = max(1, mp.cpu_count() - 2)
    if num_workers < 1:
        num_workers = 1
    process_pairs = partial(
        process_image_mask_pair,
        resize_size=resize_size,
        feature_extractor=feature_extractor,
        verbose=verbose,
        frd_version=frd_version,
    )
    with mp.Pool(processes=num_workers) as p:
        results = p.map(process_pairs, zip(files, mask_paths))

    features_list = []
    list_radiomics_feature_dict_list = []

    for i, result in enumerate(results):
        if result is None:
            logging.warning(f"Feature extraction skipped for image {files[i]}")
            continue
        features_list.append(result[0])
        list_radiomics_feature_dict_list.append(result[1])

    if not features_list:
        raise RuntimeError("No features could be extracted from any image.")

    if verbose:
        logging.info(f"Number of radiomics features: {len(features_list[0])}")

    pred_arr = np.asarray(features_list)
    return pred_arr, list_radiomics_feature_dict_list, mask_paths


