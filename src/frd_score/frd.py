def get_feature_extractor_v0(features, settings_dict: dict = None):
    """Returns a pyradiomics feature extractor for v0.

    Constructs the extractor from a dict of feature class names and optional settings.
    """
    if isinstance(features, str) and features.endswith(".csv"):
        raise NotImplementedError(
            "Feature extraction based on a csv file is not yet implemented. "
            "Please open an issue: https://github.com/RichardObi/frd/issues/new/choose."
        )

    settings = {}
    if settings_dict is None:
        settings_dict = V0_RADIOMICS_SETTINGS
    settings["setting"] = settings_dict

    # Set feature classes to compute
    settings["featureClass"] = {feature: [] for feature in features}
    _ensure_radiomics()
    return featureextractor.RadiomicsFeatureExtractor(settings)


