#  Post-extraction feature filtering
# ─────────────────────────────────────────────────────────────────────────────


def _apply_exclude_features(feature_arrays, feature_names, exclude_features):
    """Remove features matching the specified categories from all arrays.

    Params:
    -- feature_arrays   : list of np.ndarray, each (n_samples, n_features)
    -- feature_names    : list of str, feature names corresponding to columns
    -- exclude_features : list of str from EXCLUDE_OPTIONS

    Returns:
    -- filtered_arrays : list of np.ndarray with excluded columns removed
    -- filtered_names  : list of str with excluded names removed
    """
    if not exclude_features:
        return feature_arrays, feature_names

    keep_mask = np.ones(len(feature_names), dtype=bool)

    for excl in exclude_features:
        if excl == EXCLUDE_TEXTURAL:
            # Texture features: glcm, glrlm, glszm, ngtdm, gldm
            for idx, name in enumerate(feature_names):
                parts = name.split("_")
                if len(parts) >= 2:
                    cls_part = parts[0].split("-")[-1] if "-" in parts[0] else parts[0]
                    if cls_part.startswith("gl") or cls_part == "ngtdm":
                        keep_mask[idx] = False
        elif excl == EXCLUDE_WAVELET:
            for idx, name in enumerate(feature_names):
                if name.startswith("wavelet"):
                    keep_mask[idx] = False
        elif excl == EXCLUDE_FIRSTORDER:
            for idx, name in enumerate(feature_names):
                if "firstorder" in name:
                    keep_mask[idx] = False
        elif excl == EXCLUDE_SHAPE:
            # Shape features: shape_* and shape2D_* (often constant without masks)
            # Also catches wavelet-prefixed variants like wavelet-LLH_shape_*
            for idx, name in enumerate(feature_names):
                parts = name.split("_")
                if len(parts) >= 2:
                    for p in parts[:-1]:
                        clean = p.split("-")[-1] if "-" in p else p
                        if clean == "shape" or clean == "shape2D":
                            keep_mask[idx] = False
                            break

    filtered_names = [n for n, k in zip(feature_names, keep_mask) if k]
    filtered_arrays = [arr[:, keep_mask] for arr in feature_arrays]

    n_removed = int(np.sum(~keep_mask))
    if n_removed > 0:
        logging.info(
            f"exclude_features={exclude_features}: removed {n_removed} features, "
            f"{len(filtered_names)} remaining."
        )

    return filtered_arrays, filtered_names


def _apply_match_sample_count(feature_list):
    """Subsample the larger dataset to match the smaller one.

    Params:
    -- feature_list : list of np.ndarray, each (n_samples, n_features)

    Returns:
    -- feature_list : list with the larger array randomly subsampled
    """
    if len(feature_list) != 2:
        return feature_list

    n0, n1 = feature_list[0].shape[0], feature_list[1].shape[0]
    if n0 > n1:
        indices = np.random.choice(n0, n1, replace=False)
        feature_list[0] = feature_list[0][indices]
        logging.info(f"match_sample_count: subsampled D1 from {n0} to {n1} samples.")
    elif n1 > n0:
        indices = np.random.choice(n1, n0, replace=False)
        feature_list[1] = feature_list[1][indices]
        logging.info(f"match_sample_count: subsampled D2 from {n1} to {n0} samples.")

    return feature_list


# ─────────────────────────────────────────────────────────────────────────────
#  Interpretability analysis
# ─────────────────────────────────────────────────────────────────────────────


def interpret_frd(
    feature_list,
    feature_names,
    viz_dir="outputs/interpretability_visualizations",
    run_tsne=True,
):
    """Run interpretability analysis on extracted radiomic features.

    Produces t-SNE visualizations and per-feature difference rankings.

    Params:
    -- feature_list  : list of two np.ndarray (n_samples, n_features), normalized features
    -- feature_names : list of str, feature names
    -- viz_dir       : output directory for visualizations
    -- run_tsne      : whether to run t-SNE embedding visualization

    Returns:
    -- dict with interpretation results (top_changed_features, etc.)
    """
    if not _HAS_MATPLOTLIB:
        raise ImportError(
            "Interpretability analysis requires matplotlib. "
            "Install with: pip install matplotlib"
        )

    os.makedirs(viz_dir, exist_ok=True)
    feats1, feats2 = feature_list[0], feature_list[1]
    results = {}

    # t-SNE visualization
    if run_tsne:
        if not _HAS_SKLEARN:
            warnings.warn(
                "t-SNE requires scikit-learn. Install with: pip install scikit-learn. "
                "Skipping t-SNE visualization.",
                UserWarning,
                stacklevel=2,
            )
        else:
            logging.info("Running t-SNE on radiomic features...")
            all_feats = np.concatenate([feats1, feats2], axis=0)
            emb = TSNE(
                n_components=2, perplexity=min(10, all_feats.shape[0] - 1), n_iter=1000
            ).fit_transform(all_feats)

            domain_labels = np.concatenate(
                [
                    np.zeros(feats1.shape[0]),
                    np.ones(feats2.shape[0]),
                ]
            )
            plt.figure(figsize=(6, 6), dpi=150)
            for label, name, color in [(0, "D1", "tab:blue"), (1, "D2", "tab:orange")]:
                mask = domain_labels == label
                plt.scatter(
                    emb[mask, 0], emb[mask, 1], label=name, alpha=0.5, c=color, s=20
                )
            plt.title("Radiomic Feature t-SNE")
            plt.legend()
            plt.tight_layout()
            tsne_path = os.path.join(viz_dir, "radiomic_tsne.png")
            plt.savefig(tsne_path, bbox_inches="tight")
            plt.close()
            logging.info(f"t-SNE plot saved to {tsne_path}")

    # Per-feature difference ranking (distribution-level)
    feature_names_arr = np.array(feature_names)
    mean_diff_sq = (np.mean(feats2, axis=0) - np.mean(feats1, axis=0)) ** 2
    sorted_idx = mean_diff_sq.argsort()[::-1]

    top_k = min(20, len(feature_names))
    top_features = [
        (feature_names_arr[i], float(mean_diff_sq[i])) for i in sorted_idx[:top_k]
    ]
    results["top_changed_features"] = top_features

    # Plot per-feature differences
    plt.figure(figsize=(5, 3), dpi=150)
    x = np.arange(len(mean_diff_sq))
    plt.plot(x, mean_diff_sq[sorted_idx], "-", color="indianred")
    plt.yscale("log")
    plt.title("Per-feature mean difference (sorted)")
    plt.xlabel("sorted feature index")
    plt.ylabel("$|\\Delta \\mu|^2$")
    plt.grid(True, alpha=0.3)
    feat_diff_path = os.path.join(viz_dir, "sorted_feature_differences.png")
    plt.savefig(feat_diff_path, bbox_inches="tight")
    plt.close()

    # Print top features
    print(f"\nTop {top_k} changed radiomic features:")
    for name, val in top_features:
        print(f"  {name}: {val:.4f}")

    results["n_features"] = len(feature_names)
    return results


